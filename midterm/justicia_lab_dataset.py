def generate_worker_name(company_name):
    first_names = ["Maria", "James", "Ana", "David", "Sofia", "Luis", "Elena", "Daniel"]
    last_initials = ["R.", "M.", "T.", "G.", "L.", "C.", "P.", "S."]
    seed = sum(ord(char) for char in str(company_name))
    first = first_names[seed % len(first_names)]
    last = last_initials[seed % len(last_initials)]
    return f"{first} {last}"

